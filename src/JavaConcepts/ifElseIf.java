package JavaConcepts;

public class ifElseIf {

	public static void main(String[] args) {
		int marks = 120;
		if(marks<90) {
			System.out.println("pass");
			
		}
		else if(marks<70){
			System.out.println("Fail");
		}
		else if(marks<50){
			System.out.println("good");
		}
		else {
			System.out.println("hello");
		}
		

	}

	

}
